# Installation script in PowerShell

# Install @types/node as dev dependency
npm install @types/node --save-dev

# Install typescript as dev dependency
npm install typescript --save-dev

# Initialize a new npm package with default options
npm init -y

# Install @playwright/test as dev dependency
npm install -D @playwright/test

# Install playwright as dev dependency
npm install --save-dev playwright

# Install playwright and @playwright/test
# This may be redundant since the previous two commands have already installed these packages.
npm install playwright @playwright/test

# Install xml2js as dev dependency
npm install xml2js --save-dev

# Initialize TypeScript configuration
npx tsc --init

# Install node reference
npm install --save-dev @types/node

# For TypeScript reflection
npm install reflect-metadata --save

# For web services
npm install axios

# JSON tool
npm install json5
